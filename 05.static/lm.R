# --------------------------------------------------------------------
# 회귀분석으로 나이, 학습시간, 수학점수간 관계 분석하기
# --------------------------------------------------------------------

# 데이터프레임 생성
students <- data.frame(
  학번 = 1:10,
  이름 = c("홍길동", "김영희", "이철수", "박영수", "최지혜", 
         "한지민", "강하늘", "조정석", "배수지", "김수현"),
  나이 = c(23, 25, 22, 24, 23, 24, 22, 25, 23, 24),
  학습시간 = c(10, 9, 12, 8, 11, 13, 7, 8, 9, 5),  # 독립 변수
  수학점수 = c(85, 88, 90, 80, 85, 95, 78, 82, 84, 75)  # 종속 변수
)

# 데이터프레임 출력
print(students)

# 회귀분석 수행
회귀모델 <- lm(수학점수 ~ 학습시간, data = students)

# 회귀분석 결과 출력
summary(회귀모델)

